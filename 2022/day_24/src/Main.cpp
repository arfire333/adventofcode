const int SCALE = 20;
const int TARGET_FPS = 10;
int main(int argc, char **argv) {
    Grid g;
    g.init(MINE_IN, SCALE);

    SetConfigFlags(FLAG_WINDOW_RESIZABLE);
    auto windowSize = g.windowSize();
    Image elfImage = LoadImageFromMemory(".png",ELF_DATA, ELF_SIZE);
    InitWindow(windowSize.x, windowSize.y+2*elfImage.height, argv[0]);   
    
    Texture2D elf = LoadTextureFromImage(elfImage);
    g.setElf(elf);
    SetExitKey(0);
    SetTargetFPS(TARGET_FPS);
    stringstream ss;

    int tick=0;
    while (!WindowShouldClose()) {
        tick++;
        if( tick == g.minPathLength() ){
            tick=0;
        }
        BeginDrawing();
        ClearBackground(BLACK);        
        ss << tick << " of " << g.minPathLength() << "\n";        
        g.draw(tick);
        DrawText(ss.str().c_str(), (windowSize.x-SCALE*ss.str().size())/2,0,SCALE , BLACK);
        EndDrawing();
    }

    CloseWindow();
    return 0;
}
